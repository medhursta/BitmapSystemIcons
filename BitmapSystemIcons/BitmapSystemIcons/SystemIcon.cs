        public static Image Get(Icons icon)
        {
            var resource = GetResource(icon);
            Stream stream = new MemoryStream(resource);
            var svgDoc = SvgDocument.Open<SvgDocument>(stream);
            var bitmap = svgDoc.Draw();
            var output = new MemoryStream();
            bitmap.Save(output, ImageFormat.Png);
            output.Position = 0;
            var outputImage = new Bitmap(output);
            return outputImage; ;
        }

